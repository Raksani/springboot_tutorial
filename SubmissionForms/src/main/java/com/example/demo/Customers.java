package com.example.demo;

public class Customers {
    private int cid;
    private int cname;
    private int email;

	public int getCid() {
		return this.cid;
	}

	public void setCid(int cid) {
		this.cid = cid;
	}

	public int getCname() {
		return this.cname;
	}

	public void setCname(int cname) {
		this.cname = cname;
	}

	public int getEmail() {
		return this.email;
	}

	public void setEmail(int email) {
		this.email = email;
	}

    

}